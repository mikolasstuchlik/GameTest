import CLibs

final class Map {

    func loadFrom(file: Assets.Map) throws {
        let parsed = try String.init(contentsOf: file.url)
            .components(separatedBy: "\n")
            .map { $0.components(separatedBy: " ") }

        self.loadMap { tile in
            return Int(parsed[tile.y][tile.x])!
        }
    }

    func loadMap(_ loader: (_ tile: Point<Int>) -> Int) {
        for x in 0..<Map.dimensions.width {
            for y in 0..<Map.dimensions.height {
                let point = Point(x: x, y: y)
                map[mapIndex(for: point)] = loader(point)
            }
        } 
    }

    func drawMap() {
        var destination = SDL_Rect(x: 0, y: 0, w: 32, h: 32)

        for x in 0..<Map.dimensions.width {
            for y in 0..<Map.dimensions.height {
                let content = map[mapIndex(for: Point(x: x, y: y))]
                destination.x = CInt(Map.tileDimensions.width * x)
                destination.y = CInt(Map.tileDimensions.height * y)

                switch content {
                case 0:
                    try! Application.renderer!.render(water, source: nil, destination: destination)
                case 1:
                    try! Application.renderer!.render(plains, source: nil, destination: destination)
                case 2:
                    try! Application.renderer!.render(sand, source: nil, destination: destination)
                default: break
                }
            }
        }
    }
